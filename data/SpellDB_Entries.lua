-- ==========================================================================
-- data/SpellDB_Entries.lua
-- ==========================================================================
-- Purpose:  Authored spell templates for the spell database.
-- Owns:     Static spell data in template format. Each template groups all
--           rank IDs for one spell and defines shared properties once.
-- Does NOT: Contain logic, derived fields, or Init code.
-- Used by:  SpellDB:Init reads these templates, expands them into per-ID
--           entries, and enriches with DBC data. Feature handlers look up
--           entries by spellId to decide what to track.
-- Calls:    Nothing. Pure data declarations.
-- ==========================================================================
--
-- Format:
--   Each entry in AzF.SpellTemplates is keyed by a readable spell name.
--   Fields at the top level are shared across all rank IDs.
--
--   ids = { ... }  lists rank spellIds. Each element is either:
--     - A plain number:  inherits all base fields.
--     - A table:         [1] = spellId, remaining keys override base fields.
--
--   classPriority = { CLASS = N }  (optional) inline class-specific priority.
--
--   resetsSpells = { "TemplateName", ... }  references other template keys.
--   Init resolves these to the union of all rank IDs from those templates.
-- ==========================================================================

